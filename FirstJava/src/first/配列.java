package first;

public class 配列 {
	public static void main(String[] args) {

		//age配列を定義
		int [] age = new int[5];

		//age配列の添え字が3の部分に30を代入
		age[3] = 30;

		//各配列の中身を出力する
		System.out.println(age[0]);
		System.out.println(age[1]);
		System.out.println(age[2]);
		System.out.println(age[3]);
		System.out.println(age[4]);


	}
}
